/* VARIABLE AND DATA TYPES */

// STRING
let myFirstJob = 'Developer';
console.log(myFirstJob);

let myCurrentJob = 'Mentor';
console.log(myCurrentJob);

// NUMBER
let age = 28;
console.log(age);

// BOOLEAN
let programmingIsFun = true;
console.log(programmingIsFun);

// REASSIGN A VALUE
programmingIsFun = 'Yes!'
console.log(programmingIsFun);

// UNDEFINED
let year;
console.log(year);

// NULL
let x = null
console.log(x);
